# Integer to Float
a = 10
b = float(a)
print("Integer to Float:", b)

# Float to Integer
x = 5.8
y = int(x)
print("Float to Integer:", y)

# Integer to String
num = 100
s = str(num)
print("Integer to String:", s)

# String to Integer
s1 = "25"
n1 = int(s1)
print("String to Integer:", n1)

# String to Float
s2 = "3.14"
f1 = float(s2)
print("String to Float:", f1)